/// USB RNDIS network manager.
pub struct NetworkManager {
    /// Current usb0 interface state.
    pub usb0_state: Usb0State,
    /// Known IP addresses on usb0.
    pub usb0_ips: Vec<String>,
    /// Internet connectivity status.
    pub internet: InternetStatus,
    /// Last time we successfully applied IPs.
    pub last_ip_apply: Option<Instant>,
    /// Last time we checked internet connectivity.
    pub last_inet_check: Option<Instant>,
    /// Number of consecutive health check failures.
    pub consecutive_failures: u32,
    /// Log of connectivity state changes.
    pub last_state_change: Option<Instant>,
}

impl NetworkManager {
    /// Create a new network manager.
    pub fn new() -> Self {
        Self {
            usb0_state: Usb0State::Absent,
            usb0_ips: Vec::new(),
            internet: InternetStatus::Unknown,
            last_ip_apply: None,
            last_inet_check: None,
            consecutive_failures: 0,
            last_state_change: None,
        }
    }

    /// Probe usb0 interface state and update internal state.
    pub fn probe(&mut self) -> Usb0State {
        if !usb0_exists() {
            if self.usb0_state != Usb0State::Absent {
                info!("usb0 disappeared");
                self.on_state_change();
            }
            self.usb0_state = Usb0State::Absent;
            self.usb0_ips.clear();
            self.internet = InternetStatus::Offline;
            return self.usb0_state;
        }

        let new_state = read_operstate();
        if new_state != self.usb0_state {
            info!("usb0 state: {:?} -> {:?}", self.usb0_state, new_state);
            self.on_state_change();
        }
        self.usb0_state = new_state;

        // Refresh IPs
        self.refresh_ips();

        self.usb0_state
    }

    /// Refresh the known IP addresses on usb0.
    pub fn refresh_ips(&mut self) {
        #[cfg(unix)]
        {
            let args = build_ip_addr_show_args();
            if let Ok(output) = run_ip(&args) {
                self.usb0_ips = parse_ips_from_output(&output);
            }
        }
    }

    /// Apply the static dual-IP configuration on usb0.
    ///
    /// Sets 10.0.0.2/24 and 192.168.137.2/24 if missing.
    /// Also brings the link up and sets default gateway + DNS.
    pub fn apply_ip_config(&mut self) -> Result<(), String> {
        if self.usb0_state == Usb0State::Absent {
            return Err("usb0 interface does not exist".into());
        }

        // Bring link up first
        #[cfg(unix)]
        {
            let args = build_link_up_args();
            let _ = run_ip(&args);
        }

        let (need_primary, need_ics) = missing_ips(&self.usb0_ips);

        if need_primary {
            info!("adding primary IP {USB_CIDR_PRIMARY} to usb0");
            #[cfg(unix)]
            {
                let args = build_ip_add_args(USB_CIDR_PRIMARY);
                if let Err(e) = run_ip(&args) {
                    warn!("ip addr add {USB_CIDR_PRIMARY}: {e}");
                }
            }
        }

        if need_ics {
            info!("adding ICS IP {USB_CIDR_ICS} to usb0");
            #[cfg(unix)]
            {
                let args = build_ip_add_args(USB_CIDR_ICS);
                if let Err(e) = run_ip(&args) {
                    warn!("ip addr add {USB_CIDR_ICS}: {e}");
                }
            }
        }

        // Set default gateway
        #[cfg(unix)]
        {
            let args = build_default_route_replace_args(USB_GATEWAY);
            if let Err(e) = run_ip(&args) {
                warn!("ip route replace default: {e}");
            }
        }

        // Set DNS
        #[cfg(unix)]
        {
            if let Err(e) = write_resolv_conf(DNS_SERVER) {
                warn!("write resolv.conf: {e}");
            }
        }

        self.last_ip_apply = Some(Instant::now());

        // Refresh IPs after applying
        self.refresh_ips();

        if need_primary || need_ics {
            info!(
                "IP config applied: needed_primary={need_primary}, needed_ics={need_ics}"
            );
        }

        Ok(())
    }

    /// Check internet connectivity via ping + DNS.
    ///
    /// Returns the new status.
    pub fn check_internet(&mut self) -> InternetStatus {
        // Ping regardless of usb0 state — internet may come via bnep0 (BT tether)
        let ping_ok;
        #[cfg(unix)]
        {
            let args = build_ping_args(PING_TARGET, 3);
            ping_ok = run_ping(&args);
        }
        #[cfg(not(unix))]
        {
            ping_ok = false;
        }

        let old = self.internet;
        self.internet = if ping_ok {
            InternetStatus::Online
        } else {
            InternetStatus::Offline
        };

        if self.internet != old {
            info!("internet: {:?} -> {:?}", old, self.internet);
            self.on_state_change();
        }

        self.last_inet_check = Some(Instant::now());
        self.internet
    }

    /// Run DNS resolution test. Returns true if DNS works.
    pub fn check_dns(&self) -> bool {
        #[cfg(unix)]
        {
            let args = build_dns_test_args(DNS_TEST_HOST);
            return run_getent(&args);
        }
        #[cfg(not(unix))]
        {
            false
        }
    }

    /// Detect if ICS reassigned our IP via DHCP.
    pub fn detect_ics_change(&self) -> bool {
        detect_ics_dhcp(&self.usb0_ips)
    }

    /// Periodic health check: re-apply IPs if they disappeared.
    ///
    /// Call this once per epoch.
    pub fn health_check(&mut self) {
        let prev_state = self.usb0_state;
        self.probe();

        if self.usb0_state == Usb0State::Absent {
            self.consecutive_failures = 0;
            return;
        }

        let (need_primary, need_ics) = missing_ips(&self.usb0_ips);
        if need_primary || need_ics {
            warn!(
                "usb0 IPs missing (primary={}, ics={}), re-applying",
                need_primary, need_ics
            );
            let _ = self.apply_ip_config();
            self.consecutive_failures += 1;
        } else {
            self.consecutive_failures = 0;
        }

        // Log if interface came back
        if prev_state == Usb0State::Absent && self.usb0_state != Usb0State::Absent {
            info!("usb0 appeared, applying IP config");
            let _ = self.apply_ip_config();
        }
    }

    /// Get USB IP display string.
    pub fn usb_ip_str(&self) -> String {
        let usb_ip = self.usb0_ips.first().map(|s| s.as_str());
        format_usb_ip(usb_ip)
    }

    /// Record a state change timestamp.
    fn on_state_change(&mut self) {
        self.last_state_change = Some(Instant::now());
    }

    /// Short status string for logging/display.
    pub fn status_str(&self) -> &'static str {
        match (self.usb0_state, self.internet) {
            (Usb0State::Absent, _) => "USB --",
            (Usb0State::Down, _) => "USB DN",
            (Usb0State::Up, InternetStatus::Online) => "USB OK",
            (Usb0State::Up, InternetStatus::Offline) => "USB NI",
            (Usb0State::Up, InternetStatus::Unknown) => "USB UP",
        }
    }
}

impl Default for NetworkManager {
    fn default() -> Self {
        Self::new()
    }
}


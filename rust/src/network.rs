/// Build `ip addr show usb0` args.
pub fn build_ip_addr_show_args() -> Vec<String> {
    vec!["-4".into(), "addr".into(), "show".into(), "usb0".into()]
}

/// Build `ip addr add <cidr> dev usb0` args.
pub fn build_ip_add_args(cidr: &str) -> Vec<String> {
    vec![
        "addr".into(),
        "add".into(),
        cidr.into(),
        "dev".into(),
        "usb0".into(),
    ]
}

/// Build `ip link set usb0 up` args.
pub fn build_link_up_args() -> Vec<String> {
    vec!["link".into(), "set".into(), "usb0".into(), "up".into()]
}

/// Build `ip route add default via <gw> dev usb0` args.
pub fn build_default_route_args(gateway: &str) -> Vec<String> {
    vec![
        "route".into(),
        "add".into(),
        "default".into(),
        "via".into(),
        gateway.into(),
        "dev".into(),
        "usb0".into(),
    ]
}

/// Build `ip route replace default via <gw> dev usb0` args.
pub fn build_default_route_replace_args(gateway: &str) -> Vec<String> {
    vec![
        "route".into(),
        "replace".into(),
        "default".into(),
        "via".into(),
        gateway.into(),
        "dev".into(),
        "usb0".into(),
    ]
}

/// Build ping command args: `ping -c 1 -W <timeout_secs> <target>`.
pub fn build_ping_args(target: &str, timeout_secs: u32) -> Vec<String> {
    vec![
        "-c".into(),
        "1".into(),
        "-W".into(),
        timeout_secs.to_string(),
        target.into(),
    ]
}

/// Build DNS resolution test args: `getent hosts <hostname>`.
pub fn build_dns_test_args(hostname: &str) -> Vec<String> {
    vec!["hosts".into(), hostname.into()]
}

/// Build the resolv.conf content with a nameserver entry.
pub fn build_resolv_conf(dns: &str) -> String {
    format!("# Generated by oxigotchi\nnameserver {dns}\n")
}

/// Parse all IPv4 addresses from `ip -4 addr show usb0` output.
///
/// Returns a list of bare IPs (no CIDR suffix).
pub fn parse_ips_from_output(output: &str) -> Vec<String> {
    let mut ips = Vec::new();
    for line in output.lines() {
        let trimmed = line.trim();
        if let Some(rest) = trimmed.strip_prefix("inet ") {
            if let Some(addr_cidr) = rest.split_whitespace().next() {
                if let Some(addr) = addr_cidr.split('/').next() {
                    ips.push(addr.to_string());
                }
            }
        }
    }
    ips
}

/// Check if a list of IPs contains a specific address.
pub fn has_ip(ips: &[String], target: &str) -> bool {
    ips.iter().any(|ip| ip == target)
}

/// Detect if ICS changed our IP (we got a 192.168.137.x address from DHCP
/// instead of our static config).
pub fn detect_ics_dhcp(ips: &[String]) -> bool {
    ips.iter()
        .any(|ip| ip.starts_with(ICS_SUBNET_PREFIX) && ip != USB_IP_ICS)
}

/// Parse usb0 link operstate from sysfs content.
///
/// The file contains one of: "up", "down", "unknown", etc.
pub fn parse_operstate(content: &str) -> Usb0State {
    match content.trim() {
        "up" => Usb0State::Up,
        _ => Usb0State::Down,
    }
}

/// Determine which IPs are missing from the current configuration.
///
/// Returns `(need_primary, need_ics)`.
pub fn missing_ips(current_ips: &[String]) -> (bool, bool) {
    let need_primary = !has_ip(current_ips, USB_IP_PRIMARY);
    let need_ics = !has_ip(current_ips, USB_IP_ICS);
    (need_primary, need_ics)
}


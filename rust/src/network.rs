//! USB gadget RNDIS network management.
//!
//! Manages the usb0 interface: static IP configuration, internet
//! connectivity checks, IP display rotation for the e-ink screen,
//! and periodic health monitoring.
//!
//! Replaces `usb0-fallback.service` and the NM connection profile.

use log::{info, warn};
use std::time::Instant;

// ---------------------------------------------------------------------------
// Constants
// ---------------------------------------------------------------------------

/// sysfs path for usb0 interface existence check.
pub const USB0_SYSFS_PATH: &str = "/sys/class/net/usb0";
/// sysfs path for usb0 link operstate.
pub const USB0_OPERSTATE_PATH: &str = "/sys/class/net/usb0/operstate";
/// Primary static IP for direct USB RNDIS (Linux side).
pub const USB_IP_PRIMARY: &str = "10.0.0.2";
/// Primary CIDR for the direct RNDIS subnet.
pub const USB_CIDR_PRIMARY: &str = "10.0.0.2/24";
/// Secondary static IP for Windows ICS subnet.
pub const USB_IP_ICS: &str = "192.168.137.2";
/// Secondary CIDR for the ICS subnet.
pub const USB_CIDR_ICS: &str = "192.168.137.2/24";
/// Default gateway via the RNDIS host.
pub const USB_GATEWAY: &str = "10.0.0.1";
/// DNS server.
pub const DNS_SERVER: &str = "8.8.8.8";
/// Ping target for internet connectivity check.
pub const PING_TARGET: &str = "8.8.8.8";
/// DNS resolution test hostname.
pub const DNS_TEST_HOST: &str = "google.com";
/// ICS subnet prefix (Windows Internet Connection Sharing).
pub const ICS_SUBNET_PREFIX: &str = "192.168.137.";

// ---------------------------------------------------------------------------
// USB0 interface state
// ---------------------------------------------------------------------------

/// Whether the usb0 interface exists and its link state.
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum Usb0State {
    /// Interface does not exist (no USB gadget / not connected).
    Absent,
    /// Interface exists but link is DOWN.
    Down,
    /// Interface exists and link is UP (RNDIS connected).
    Up,
}

/// Internet connectivity status.
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum InternetStatus {
    /// Not checked yet.
    Unknown,
    /// Ping succeeds, internet reachable.
    Online,
    /// Ping fails, no internet.
    Offline,
}

/// Format USB IP for e-ink display. Always shown.
pub fn format_usb_ip(ip: Option<&str>) -> String {
    if let Some(ip) = ip {
        format!("USB:{ip} :8080")
    } else {
        "USB:--".to_string()
    }
}

/// Format BT IP for e-ink display. Empty when not connected.
pub fn format_bt_ip(ip: Option<&str>) -> String {
    if let Some(ip) = ip {
        format!("BT:{ip}")
    } else {
        String::new()
    }
}


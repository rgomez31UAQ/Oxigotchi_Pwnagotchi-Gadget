// System command execution (unix-only)
// ---------------------------------------------------------------------------

/// Run `ip` with the given arguments. Returns Ok(stdout) or Err(stderr).
#[cfg(unix)]
fn run_ip(args: &[String]) -> Result<String, String> {
    let output = std::process::Command::new("ip")
        .args(args)
        .output()
        .map_err(|e| format!("failed to run ip: {e}"))?;
    if output.status.success() {
        Ok(String::from_utf8_lossy(&output.stdout).to_string())
    } else {
        Err(String::from_utf8_lossy(&output.stderr).to_string())
    }
}

/// Run `ping` with the given arguments. Returns true if exit code 0.
#[cfg(unix)]
fn run_ping(args: &[String]) -> bool {
    std::process::Command::new("ping")
        .args(args)
        .stdout(std::process::Stdio::null())
        .stderr(std::process::Stdio::null())
        .status()
        .map(|s| s.success())
        .unwrap_or(false)
}

/// Run `getent` with the given arguments. Returns true if exit code 0.
#[cfg(unix)]
fn run_getent(args: &[String]) -> bool {
    std::process::Command::new("getent")
        .args(args)
        .stdout(std::process::Stdio::null())
        .stderr(std::process::Stdio::null())
        .status()
        .map(|s| s.success())
        .unwrap_or(false)
}

/// Check if usb0 interface exists (unix-only).
#[cfg(unix)]
fn usb0_exists() -> bool {
    std::path::Path::new(USB0_SYSFS_PATH).exists()
}

/// Stub: usb0 never exists on non-unix.
#[cfg(not(unix))]
fn usb0_exists() -> bool {
    false
}

/// Read usb0 operstate from sysfs (unix-only).
#[cfg(unix)]
fn read_operstate() -> Usb0State {
    match std::fs::read_to_string(USB0_OPERSTATE_PATH) {
        Ok(content) => parse_operstate(&content),
        Err(_) => Usb0State::Absent,
    }
}

/// Stub: always Absent on non-unix.
#[cfg(not(unix))]
fn read_operstate() -> Usb0State {
    Usb0State::Absent
}

/// Write DNS config to /etc/resolv.conf (unix-only).
#[cfg(unix)]
fn write_resolv_conf(dns: &str) -> Result<(), String> {
    let content = build_resolv_conf(dns);
    std::fs::write("/etc/resolv.conf", content)
        .map_err(|e| format!("failed to write /etc/resolv.conf: {e}"))
}

